package com.akinfopark.colgbustracking;

public class EmployeeInfo {

    private String employeeName;
    private String employeeContactNumber;
    private String employeeAddress;
    private String empEmail;
    private String empLat;
    private String empLong;
    private String empType;
    private String empBusNum;
    private String empFcm;

    public String getEmpFcm() {
        return empFcm;
    }

    public void setEmpFcm(String empFcm) {
        this.empFcm = empFcm;
    }

    public EmployeeInfo() {

    }

    public String getEmpEmail() {
        return empEmail;
    }

    public void setEmpEmail(String empEmail) {
        this.empEmail = empEmail;
    }

    public String getEmpLat() {
        return empLat;
    }

    public void setEmpLat(String empLat) {
        this.empLat = empLat;
    }

    public String getEmpLong() {
        return empLong;
    }

    public void setEmpLong(String empLong) {
        this.empLong = empLong;
    }

    public String getEmpType() {
        return empType;
    }

    public void setEmpType(String empType) {
        this.empType = empType;
    }

    public String getEmpBusNum() {
        return empBusNum;
    }

    public void setEmpBusNum(String empBusNum) {
        this.empBusNum = empBusNum;
    }

    // created getter and setter methods
    // for all our variables.
    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeeContactNumber() {
        return employeeContactNumber;
    }

    public void setEmployeeContactNumber(String employeeContactNumber) {
        this.employeeContactNumber = employeeContactNumber;
    }

    public String getEmployeeAddress() {
        return employeeAddress;
    }

    public void setEmployeeAddress(String employeeAddress) {
        this.employeeAddress = employeeAddress;
    }
}
